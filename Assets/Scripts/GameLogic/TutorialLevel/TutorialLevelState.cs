/// <summary>
/// Enum that defines the different tutorial level states
/// </summary>
public enum TutorialLevelState
{
    /// <summary>
    /// Tutorial level started
    /// </summary>
    Started,
    /// <summary>
    /// Small fish spawned
    /// </summary>
    SmallFishSpawned,
    /// <summary>
    /// Small fish eaten
    /// </summary>
    SmallFishEaten,
    /// <summary>
    /// First medium fish spawned
    /// </summary>
    MediumFish1Eaten,
    /// <summary>
    /// Big fish spawned
    /// </summary>
    BigFishSpawned
}

